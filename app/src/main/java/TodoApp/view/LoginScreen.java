    private void hideErrorLabels() {
        jLabelEmailError.setVisible(false);
        jLabelPasswordError.setVisible(false);
        jLabelCredentialsError.setVisible(false);
    }

    private boolean inputsAreBlank() {
        return "".equals(jTextFieldEmail.getText())
                || jPasswordFieldPassword.getPassword().length == 0;
    }

    private void goToSignUpScreen() {
        SignUpScreen signUpScreen = new SignUpScreen();
        signUpScreen.setVisible(true);
        this.dispose();
    }

    private void goToMainScreen() {
        MainScreen mainScreen = new MainScreen();
        mainScreen.setVisible(true);
        mainScreen.setAuthUser(this.user);
        this.dispose();
    }
