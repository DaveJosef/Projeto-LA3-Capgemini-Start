    private void jLabelButtonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelButtonLoginMouseClicked
        // TODO add your handling code here:
        try {
            
            User user = null;
            
            if (!inputsAreBlank()) {

                user = userController.getByEmail(jTextFieldEmail.getText());
                if (user == null) {
                    //JOptionPane.showMessageDialog(rootPane, message);
                    hideErrorLabels();
                    showErrorLabel(jLabelCredentialsError);
                    return;
                }
                
                if (!user.getPassword().equals(new String(jPasswordFieldPassword.getPassword()))) {
                    //JOptionPane.showMessageDialog(rootPane, message);
                    hideErrorLabels();
                    showErrorLabel(jLabelCredentialsError);
                    return;
                }
                
                this.user = user;
                goToMainScreen();

            } else {
                
                hideErrorLabels();
                if ("".equals(jTextFieldEmail.getText())) {
                    showErrorLabel(jLabelEmailError);
                }
                
                if (jPasswordFieldPassword.getPassword().length == 0) {
                    showErrorLabel(jLabelPasswordError);
                }
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jLabelButtonLoginMouseClicked

