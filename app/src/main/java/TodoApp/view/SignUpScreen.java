    private boolean passwordsMatch() {
        return Arrays.equals(jPasswordFieldPassword.getPassword(), jPasswordFieldPasswordConfirmation.getPassword());
    }

    private boolean inputsAreBlank() {
        return "".equals(jTextFieldName.getText())
                || "".equals(jTextFieldEmail.getText())
                || jPasswordFieldPassword.getPassword().length == 0;
    }

    private void goToLoginScreen() {
        LoginScreen loginScreen = new LoginScreen();
        loginScreen.setVisible(true);
        this.dispose();
    }
