    private void jLabelButtonSignUpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelButtonSignUpMouseClicked
        // TODO add your handling code here:
        try {
            
            User user = null;
            
            if (!"".equals(jTextFieldEmail.getText())) {
                
                user = userController.getByEmail(jTextFieldEmail.getText());
                if (user != null) {
                    JOptionPane.showMessageDialog(rootPane, "E-mail já é cadastrado");
                    return;
                }
                
            }
            
            if (!inputsAreBlank() && passwordsMatch()) {

                user = new User();
                
                user.setName(jTextFieldName.getText());
                user.setEmail(jTextFieldEmail.getText());
                user.setPassword(new String(jPasswordFieldPassword.getPassword()));

                userController.save(user);
                goToLoginScreen();

            } else {
                
                hideErrorLabels();
                if ("".equals(jTextFieldName.getText())) {
                    showErrorLabel(jLabelNameError);
                }
                
                if ("".equals(jTextFieldEmail.getText())) {
                    showErrorLabel(jLabelEmailError);
                }
                
                if (jPasswordFieldPassword.getPassword().length == 0) {
                    showErrorLabel(jLabelPasswordError);
                }
                
                if (!passwordsMatch()) {
                    showErrorLabel(jLabelPasswordConfirmationError);
                }
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jLabelButtonSignUpMouseClicked

